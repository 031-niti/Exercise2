import java.util.Scanner;
public class Exercise2_4 {
     public static void main(String[] args) {
          int a = 16, b = 25;
          System.out.println("Before swapping : a= " + a +", b= " + b);
          int temp = a;
          a = b ;
          b = temp;
          System.out.print("After swapping : a= " + a +", b= " + b);
     }
     
}
